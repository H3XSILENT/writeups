#!/bin/python3

code = bytearray([
    0xa9, 0xff, #lda #$ff
    0x8d, 0x02, 0x60, #sda #$6002
    
    0xa9, 0x55, #lda #$55
    0x8d, 0x00, 0x60, #sda #$6000

    0xa9, 0xaa, #lda #$aa
    0x8d, 0x00, 0x60,

    0x4d, 0x00, 0x80 #jmp #$8000
    ])

rom = code + bytearray(b'\xea' * (3768 - len(code)))

rom[0x7ffc] = 0x00
rom[0x7ffd] = 0x80

with open("var.bin", "wb") as out_file:
          out_file.write(rom);
